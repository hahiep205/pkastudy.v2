function buildExample(phrase, mean) {
  return {
    example: `We use "${phrase}" in daily conversation.`,
    example_vi: `Chúng ta dùng "${mean}" trong giao tiếp hằng ngày.`,
  };
}

function chunkPhrases(items, chunkSize) {
  const result = [];
  for (let index = 0; index < items.length; index += chunkSize) {
    result.push(items.slice(index, index + chunkSize));
  }
  return result;
}

function buildLesson(phrases, lessonIndex) {
  const words = phrases.map((item, phraseIndex) => ({
    id: `phr-${lessonIndex + 1}-${phraseIndex + 1}`,
    word: item.phrase,
    mean: item.mean,
    transcription: '',
    wordtype: 'phrase',
    language: 'en',
    ...buildExample(item.phrase, item.mean),
  }));

  return {
    id: `english-phrases-lesson-${lessonIndex + 1}`,
    slug: `english-phrases-lesson-${lessonIndex + 1}`,
    title: `Lesson ${lessonIndex + 1}: Common Phrases`,
    description: 'Cụm từ tiếng Anh thông dụng dùng trong giao tiếp hằng ngày.',
    sortOrder: lessonIndex + 1,
    wordCount: words.length,
    vocabularyCount: words.length,
    words,
  };
}

export const ENGLISH_PHRASES_100_LESSONS = chunkPhrases(PHRASES, 10).map((pair, lessonIndex) =>
  buildLesson(pair, lessonIndex),
);
